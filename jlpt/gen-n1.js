// Replace logo
output = output.replace('JLPT <span>N4</span>', 'JLPT <span>N1</span>');

// Replace stats bar /166
output = output.replace('/166</div>', `/${total}</div>`);

// Replace KANJI DATA comment
output = output.replace('All 166 JLPT N4 Kanji', `All ${total} JLPT N1 Kanji`);

// Replace TOTAL
output = output.replace('const TOTAL = 166;', `const TOTAL = ${total};`);

// Replace the KANJI array (bracket-counting to handle single-line arrays)
const kanji_start = output.match(/const KANJI\s*=\s*\[/);
if (kanji_start) {
  const si = kanji_start.index;
  const bi = si + kanji_start[0].length - 1;
  let depth = 1, ei = bi + 1;
  while (depth > 0 && ei < output.length) {
    if (output[ei] === '[') depth++;
    else if (output[ei] === ']') depth--;
    ei++;
  }
  while (ei < output.length && output[ei] !== ';') ei++;
  output = output.substring(0, si) + `const KANJI = [\n${kanjiDataStr}\n];` + output.substring(ei + 1);
}

// Replace localStorage key
output = output.replace(/jlpt_n4/g, 'jlpt_n1');

// Replace review header
output = output.replace('All 166 Kanji — JLPT N4', `All ${total} Kanji — JLPT N1`);

// Replace filter button
