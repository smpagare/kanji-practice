// Convert compact format to full format
function toKanjiJS(arr) {
  return arr.map(([kanji, onStr, kunStr, meaning, compounds, antonym]) => {
    const on = onStr ? onStr.split(',') : [];
    const kun = kunStr ? kunStr.split(',') : [];
    const comps = compounds.map(([word, reading, m]) => `{word:"${word}",reading:"${reading}",meaning:"${m}"}`).join(',');
    const ant = antonym ? `"${antonym}"` : 'null';
    return `  { kanji:"${kanji}", on:[${on.map(r=>`"${r}"`).join(',')}], kun:[${kun.map(r=>`"${r}"`).join(',')}], meaning:"${meaning}", compounds:[${comps}], antonym:${ant} }`;
  }).join(',\n');
}

